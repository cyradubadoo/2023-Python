#编程计算有固定工资收入的党员每月所交纳的党费。工资基数3000元及以下者，交纳工资基数的0.5%；
# 工资基数3000～5000元者，交纳工资基数的1%；工资基数在5000～10000元者，交纳工资基数的1.5%；
# 工资基数超过10000元者，交纳工资基数的2%。
income=float(input("请输入党员的工资："))
if income<=3000:
    tax=0.005*income
elif income<=5000:
    tax=0.01*income
elif income<=10000:
    tax=0.15*income
else:
    tax=0.02*income
print("该党员每月所交纳的党费为：", tax, "元")